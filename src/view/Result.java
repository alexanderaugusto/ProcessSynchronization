    public Result(Attendants attendant) {
        initComponents();

        this.attendant = attendant;

        this.resultList.setModel(resultListModel);
        
        if (this.attendant.attendantSelected == 1) {
            title.setText("Atendente 1 - Sem sincronia");
            for (Client client : this.attendant.homeScreen.clients) {
                client.attendantType = 1;
                client.resultScreen = this;
                Thread t = new Thread(client, client.getName());
                t.start();
            }
        } else {
            title.setText("Atendente 2 - Sincronizado");
            for (Client client : this.attendant.homeScreen.clients) {
                client.attendantType = 2;
                client.resultScreen = this;
                Thread t = new Thread(client, client.getName());
                t.start();
            }
        }
    }

